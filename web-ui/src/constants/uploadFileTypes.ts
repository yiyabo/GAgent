export const UPLOAD_ACCEPT_LIST = [
  'image/*',
  'application/pdf',
  ...UPLOAD_ALLOWED_EXTENSIONS,
].join(',');

export function isAllowedUploadFile(file: File): boolean {
  if (file.type.startsWith('image/')) return true;
  if (UPLOAD_ALLOWED_MIME_TYPES.has(file.type)) return true;
  const name = file.name.toLowerCase();
  return UPLOAD_ALLOWED_EXTENSIONS.some((ext) => name.endsWith(ext));
}
